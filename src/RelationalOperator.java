public class RelationalOperator {

    public static void main(String[] args) {
        System.out.println('a' > 'b');
        System.out.println('a' < 'b');
        System.out.println('a' <= 'b');
        System.out.println('a' >= 'b');
        System.out.println('a' == 'b');

        //Logical Operators
        int j = 100;
        int i=98;
        int c = 55;
        System.out.println(i>=j && i>=c);
        System.out.println(i>=j || i>=c);


    }
}
